import turtle
t=turtle.Turtle('turtle')
t.shapesize(1)
t.pensize(5)
t.setheading(0)
t.hideturtle()

t.color('red')
t.begin_fill()
t.forward(200)
t.left(120)
t.forward(200)
t.left(120)
t.forward(200)
t.end_fill()

t.setheading(0)
t.begin_fill()
t.up()
t.goto(100,-50)
t.down()
t.setheading(60)
t.forward(200)
t.left(120)
t.forward(200)
t.left(120)
t.forward(200)
t.end_fill()















