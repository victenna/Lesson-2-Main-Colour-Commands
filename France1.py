import turtle
t=turtle.Turtle()
t.hideturtle()

t.color('blue')
t.begin_fill()
t.fd(80)
t.left(90)
t.fd(200)
t.left(90)
t.fd(80)
t.left(90)
t.fd(200)
t.end_fill()

t.up()
t.goto(80,0)
t.down()
t.setheading(0)
t.color('whitesmoke')
t.begin_fill()
t.fd(80)
t.left(90)
t.fd(200)
t.left(90)
t.fd(80)
t.left(90)
t.fd(200)
t.end_fill()

t.up()
t.goto(160,0)
t.down()
t.setheading(0)
t.color('red')
t.begin_fill()
t.fd(80)
t.left(90)
t.fd(200)
t.left(90)
t.fd(80)
t.left(90)
t.fd(200)
t.end_fill()


